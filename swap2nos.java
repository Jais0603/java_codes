public class swap2nos {
    public static void main(String[] args) {
        int a=60;
        int b=45;
        int c;
        c=a;
        a=b;
        b=c;
        System.out.println("The value of a is: "+ a);
        System.out.println("The value of b is: "+ b);
    }
}


